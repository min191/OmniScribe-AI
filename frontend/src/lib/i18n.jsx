function format(message, values = {}) {
  return Object.entries(values).reduce(
    (result, [key, value]) => result.replaceAll(`{${key}}`, String(value)),
    message,
  )
}

function getInitialLanguage() {
  try {
    return localStorage.getItem('omniscribe.language') === 'en' ? 'en' : 'vi'
  } catch {
    return 'vi'
  }
}

const LanguageContext = createContext({
  language: 'vi',
  locale: 'vi-VN',
  setLanguage: () => {},
  t: (key, values) => format(COPY.vi[key] || key, values),
})

export function LanguageProvider({ children }) {
  const [language, setLanguageState] = useState(getInitialLanguage)

  useEffect(() => {
    document.documentElement.lang = language
    try {
      localStorage.setItem('omniscribe.language', language)
    } catch {
      // The interface still works when storage is unavailable.
    }
  }, [language])

  const value = useMemo(() => ({
    language,
    locale: language === 'en' ? 'en-US' : 'vi-VN',
    setLanguage: (next) => setLanguageState(next === 'en' ? 'en' : 'vi'),
    t: (key, values) => format(COPY[language][key] || COPY.vi[key] || key, values),
  }), [language])

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>
}

export function useI18n() {
  return useContext(LanguageContext)
}
